/* 書き込みポインタ位置の取得*/
uint16_t getUartWrPtr(void)
{
    uint16_t wr_ptr;

    wr_ptr = RCV_BUFF_SIZE - pUart1->hdmarx->Instance->CNDTR;
    if (wr_ptr == RCV_BUFF_SIZE) {
        wr_ptr = 0;
    }

    return wr_ptr;
}

/* 受信確認*/
uint8_t isUartRcv(void)
{
    uint8_t result;

    result = FALSE;
    if (Rd_ptr != getUartWrPtr()) {
        result = TRUE;                  // 受信有り
    }
    return result;
}

/* 受信データの取得*/
uint32_t rdUart(uint32_t *rdData)
{
    uint8_t result;

    result = FALSE;
    if(isUartRcv() == TRUE) {
        *rdData = RdBuff[Rd_ptr++];
        if (Rd_ptr >= RCV_BUFF_SIZE) {
            Rd_ptr = 0;
        }
        result = TRUE;
    }
    return result;
}
